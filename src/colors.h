/* To colorize the output strings we can use "ANSI codes on linux
 * Using these defines to make it easier across the board for
 * everyone, plus make it easier to change if we want. 
 */

#define ANSI_COLOR_BOLDBLACK   "\x1b[1;30m"
#define ANSI_COLOR_BLACK       "\x1b[1;30m"
#define ANSI_COLOR_BOLDRED     "\x1b[1;31m"
#define ANSI_COLOR_RED         "\x1b[0;31m"
#define ANSI_COLOR_BOLDGREEN   "\x1b[1;32m"
#define ANSI_COLOR_GREEN       "\x1b[0;32m"
#define ANSI_COLOR_BOLDYELLOW  "\x1b[1;33m"
#define ANSI_COLOR_YELLOW      "\x1b[0;33m"
#define ANSI_COLOR_BOLDBLUE    "\x1b[1;34m"
#define ANSI_COLOR_BLUE        "\x1b[0;34m"
#define ANSI_COLOR_BOLDMAGENTA "\x1b[1;35m"
#define ANSI_COLOR_MAGENTA     "\x1b[0;35m"
#define ANSI_COLOR_BOLDCYAN    "\x1b[1;36m"
#define ANSI_COLOR_CYAN        "\x1b[0;36m"
#define ANSI_COLOR_BOLDWHITE   "\x1b[1;37m"
#define ANSI_COLOR_WHITE       "\x1b[1;37m"
#define ANSI_COLOR_RESET       "\x1b[0m"

